/**
 * add - addition operation
 * @a: first operand
 * @b: second operand
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction operation
 * @a: first operand
 * @b: second operand
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication
 * @a: number
 * @b: multiplier
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division operation
 * @a: dividend
 * @b: divisor
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modular dividion operation
 * @a: dividend
 * @b: divisor
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
